public class new_patterns {
    
//  hill pattern and diamond pattern
public static void main(String[] args) {
    int n=1;

    for(int i=1;i<n;i++){
        for(int j=1;j<=n;j++){
            System.out.print(" ");
        }
        for(int j=i;j<n;j++){
            System.out.print("*");
        }
        for(int j=i;j<=n;j++){
            System.out.print("*");
    
        }
        System.out.println();
    
    
      }
      for(int i=1;i<n;i++){
        for(int j=1;j<=i;j++){
            System.out.print(" ");
        }
        for(int j=i;j<n;j++){
            System.out.print("*");
        }
        for(int j=i;j<=n;j++){
            System.out.print("*" );
    
        }
        System.out.println();
    
    
      }
    
    
   
  



}
    
}
